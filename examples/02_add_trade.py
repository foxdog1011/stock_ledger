"""
範例 02 – 新增交易（買進 / 賣出）
===================================

示範如何用 StockLedger.add_trade() 記錄股票交易。
買進時現金自動扣除，賣出時現金自動增加。
"""

import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent.parent))

from ledger import StockLedger

DB = Path(__file__).parent.parent / "data" / "ex02_trade.db"
DB.unlink(missing_ok=True)

ledger = StockLedger(db_path=DB)

# ────────────────────────────────────────────────────────────────────────────
# 準備資金
# ────────────────────────────────────────────────────────────────────────────
ledger.add_cash(2_000_000, date="2024-01-02", note="初始資本")
print(f"初始現金：{ledger.cash_balance(as_of='2024-01-02'):>14,.0f}\n")

# ────────────────────────────────────────────────────────────────────────────
# 買進 2330（台積電）1,000 股 @ 580
# 券商手續費：0.1425%，最低 20 元  →  580,000 × 0.001425 ≈ 826，取 826
# ────────────────────────────────────────────────────────────────────────────
ledger.add_trade(
    symbol="2330", side="buy",
    qty=1_000, price=580.0,
    date="2024-01-15",
    commission=826,
    note="買進台積電",
)
print("[OK] 買進 2330  1,000 股 @ 580  手續費 826")
print(f"  現金餘額：{ledger.cash_balance(as_of='2024-01-15'):>14,.0f}")
print(f"  持有 2330：{ledger.position('2330', as_of='2024-01-15'):>6,.0f} 股\n")

# ────────────────────────────────────────────────────────────────────────────
# 買進 2317（鴻海）5,000 股 @ 103
# ────────────────────────────────────────────────────────────────────────────
ledger.add_trade(
    symbol="2317", side="buy",
    qty=5_000, price=103.0,
    date="2024-02-01",
    commission=733,
    note="買進鴻海",
)
print("[OK] 買進 2317  5,000 股 @ 103  手續費 733")
print(f"  現金餘額：{ledger.cash_balance(as_of='2024-02-01'):>14,.0f}")
print(f"  持有 2317：{ledger.position('2317', as_of='2024-02-01'):>6,.0f} 股\n")

# ────────────────────────────────────────────────────────────────────────────
# 賣出 2330  500 股 @ 900（部分獲利了結）
# 賣出需另加 0.3% 交易稅：900 × 500 × 0.003 = 1,350
# 手續費：900 × 500 × 0.001425 ≈ 641
# 共扣：1,350 + 641 = 1,991（此處統一放 commission）
# ────────────────────────────────────────────────────────────────────────────
ledger.add_trade(
    symbol="2330", side="sell",
    qty=500, price=900.0,
    date="2024-07-01",
    commission=1_991,
    note="部分獲利了結",
)
print("[OK] 賣出 2330    500 股 @ 900  手續費+稅 1,991")
print(f"  現金餘額：{ledger.cash_balance(as_of='2024-07-01'):>14,.0f}")
print(f"  持有 2330：{ledger.position('2330', as_of='2024-07-01'):>6,.0f} 股\n")

# ────────────────────────────────────────────────────────────────────────────
# 列出所有持倉
# ────────────────────────────────────────────────────────────────────────────
print("─" * 40)
print(f"{'股票':^8} {'持有股數':>10}")
print("─" * 40)
for sym, qty in ledger.positions().items():
    print(f"  {sym:<8} {qty:>10,.0f}")
print("─" * 40)

# ────────────────────────────────────────────────────────────────────────────
# 列出交易明細
# ────────────────────────────────────────────────────────────────────────────
print()
print(f"{'日期':<12} {'股票':<6} {'買/賣':<5} {'股數':>7} {'價格':>8} {'手續費':>8}  {'備注'}")
print("─" * 64)
for t in ledger.trade_history():
    side_label = "買進" if t["side"] == "buy" else "賣出"
    print(
        f"  {t['date']:<10} {t['symbol']:<6} {side_label:<5} "
        f"{t['qty']:>7,.0f} {t['price']:>8,.1f} {t['commission']:>8,.0f}"
        f"  {t['note']}"
    )
