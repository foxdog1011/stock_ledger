"""
範例 01 – 新增現金（存入 / 提出）
=================================

示範如何用 StockLedger.add_cash() 記錄資金的進出，
以及用 cash_balance() 查詢指定日期的現金餘額。
"""

import sys
from pathlib import Path

# ── 讓範例可以直接在 stock_ledger/ 根目錄執行 ─────────────────────────────
sys.path.insert(0, str(Path(__file__).parent.parent))

from ledger import StockLedger

# 使用獨立資料庫，方便重複執行
DB = Path(__file__).parent.parent / "data" / "ex01_cash.db"
DB.unlink(missing_ok=True)          # 每次執行從頭開始

ledger = StockLedger(db_path=DB)

# ────────────────────────────────────────────────────────────────────────────
# 1. 存入初始資金
# ────────────────────────────────────────────────────────────────────────────
ledger.add_cash(2_000_000, date="2024-01-02", note="初始資本")
print("[OK] 存入 2,000,000（2024-01-02）")

# ────────────────────────────────────────────────────────────────────────────
# 2. 追加資金
# ────────────────────────────────────────────────────────────────────────────
ledger.add_cash(500_000, date="2024-04-01", note="追加投資")
print("[OK] 存入   500,000（2024-04-01）")

# ────────────────────────────────────────────────────────────────────────────
# 3. 提出部分資金
# ────────────────────────────────────────────────────────────────────────────
ledger.add_cash(-300_000, date="2024-07-01", note="部分提領")
print("[OK] 提出   300,000（2024-07-01）")

# ────────────────────────────────────────────────────────────────────────────
# 4. 查詢各時點現金餘額
# ────────────────────────────────────────────────────────────────────────────
print()
print(f"{'日期':<14} {'現金餘額':>16}")
print("─" * 32)
checkpoints = [
    "2024-01-02",
    "2024-03-31",
    "2024-04-01",
    "2024-06-30",
    "2024-07-01",
    "2024-12-31",
]
for d in checkpoints:
    balance = ledger.cash_balance(as_of=d)
    print(f"  {d}    {balance:>14,.0f}")

# ────────────────────────────────────────────────────────────────────────────
# 預期輸出
# ────────────────────────────────────────────────────────────────────────────
# 日期             現金餘額
# ────────────────────────────────
#   2024-01-02       2,000,000
#   2024-03-31       2,000,000
#   2024-04-01       2,500,000
#   2024-06-30       2,500,000
#   2024-07-01       2,200,000
#   2024-12-31       2,200,000
